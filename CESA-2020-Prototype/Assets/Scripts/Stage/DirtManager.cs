using System.Collections;
using System.Collections.Generic;
using UnityEngine;
using System;
using Common;
//==============================================================================================
public class DirtManager : MonoBehaviour
{
    //------------------------------------------------------------------------------------------
    // member variable
    //------------------------------------------------------------------------------------------
    // 汚れの最大数
    int maxDirtCount = 0;
    // 汚れの数
    int dirtCount = 0;
    // 汚れの最大量
    int maxDirtAmount = 0;
    // 汚れの量
    int dirtAmount = 0;

    // 接触中の汚れ
    List<GameObject> hitDirts = new List<GameObject>();

    //------------------------------------------------------------------------------------------
    // Awake
    //------------------------------------------------------------------------------------------
    private void Awake()
    {
        
    }

	//------------------------------------------------------------------------------------------
    // Start
	//------------------------------------------------------------------------------------------
    private void Start()
    {
        
    }

	//------------------------------------------------------------------------------------------
    // Update
	//------------------------------------------------------------------------------------------
	private void Update()
    {

        float rate = 1 - dirtAmount / (float)maxDirtAmount;
        Data.currentCleanRate = 1 - dirtAmount / (float)maxDirtAmount;
        //Debug.Log("rate:" + rate);

    }

    //------------------------------------------------------------------------------------------
    // 汚れ情報の登録
    //------------------------------------------------------------------------------------------
    public void RegisterDirt(int amount)
    {
        maxDirtCount++;
        dirtCount++;
        maxDirtAmount += amount;
        dirtAmount += amount;
    }

    //------------------------------------------------------------------------------------------
    // 汚れの掃除完了処理
    //------------------------------------------------------------------------------------------
    public void DirtCleaned(int amount)
    {
        dirtCount--;
        dirtAmount -= amount;
    }

    //------------------------------------------------------------------------------------------
    // プレイヤーの掃除判定
    //------------------------------------------------------------------------------------------
    public void SweepDirt()
    {
        foreach(var dirt in hitDirts)
        {
            var dirtController = dirt.GetComponent<DirtController>();
            dirtController.Swept();
        }
    }

    //------------------------------------------------------------------------------------------
    // プレイヤーと汚れの接触処理
    //------------------------------------------------------------------------------------------
    public void EnterDirt(GameObject dirt)
    {
        if (!hitDirts.Contains(dirt))
        {
            hitDirts.Add(dirt);
        }
    }

    //------------------------------------------------------------------------------------------
    // プレイヤーが汚れから離れる処理
    //------------------------------------------------------------------------------------------
    public void ExitDirt(GameObject dirt)
    {
        if (hitDirts.Contains(dirt))
        {
            hitDirts.Remove(dirt);
        }
    }
}
