using System.Collections;
using System.Collections.Generic;
using UnityEngine;
using System;
using Common;
//==============================================================================================
public class TutorialGoal : MonoBehaviour
{
    //------------------------------------------------------------------------------------------
    // member variable
    //------------------------------------------------------------------------------------------
    [SerializeField]
    PlayDirector playDirector;

    bool once = false;  // 一度だけイベントを通知するための変数

    //------------------------------------------------------------------------------------------
    // Awake
    //------------------------------------------------------------------------------------------
    private void Awake()
    {

    }

    //------------------------------------------------------------------------------------------
    // Start
    //------------------------------------------------------------------------------------------
    private void Start()
    {

    }

    //------------------------------------------------------------------------------------------
    // Update
    //------------------------------------------------------------------------------------------
    private void Update()
    {

    }

    //------------------------------------------------------------------------------------------
    // ゴールにプレイヤーが触れた
    //------------------------------------------------------------------------------------------
    private void OnTriggerEnter2D(Collider2D col)
    {
        if (once)
        {
            return;
        }

        if (col.tag == Player.NAME)
        {
            playDirector.TutorialGoal();
        }
    }
}
