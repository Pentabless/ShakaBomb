using System.Collections;
using System.Collections.Generic;
using UnityEngine;
using System;
using Common;
using UnityEngine.SceneManagement;
//==============================================================================================
public class PlayBGM : MonoBehaviour
{
    //------------------------------------------------------------------------------------------
    // member variable
    //------------------------------------------------------------------------------------------
    [SerializeField]
    [Header("ステージBGM")]
    AudioClip clip;
    [SerializeField]
    float volum;
    bool first = false;

    //------------------------------------------------------------------------------------------
    // Awake
    //------------------------------------------------------------------------------------------
    private void Awake()
    {
        
    }

	//------------------------------------------------------------------------------------------
    // Start
	//------------------------------------------------------------------------------------------
    private void Start()
    {
        if (volum == 0.0f)
            volum = 1.0f;
        SoundPlayer.PlayBGM(clip, volum);
    }

    //------------------------------------------------------------------------------------------
    // Update
    //------------------------------------------------------------------------------------------
    private void Update()
    {

    }

    public void OnFadeBGM()
    {
        SoundFadeController.SetFadeOutSpeed(-0.01f);
    }
}
