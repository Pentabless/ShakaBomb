using UnityEngine;
using TMPro;
//==============================================================================================
public class GameInformation : MonoBehaviour
{
    //------------------------------------------------------------------------------------------
    // member variable
    //------------------------------------------------------------------------------------------
    // コピーライト用TMP
    [SerializeField]
    private TextMeshProUGUI copyright = null;



	//------------------------------------------------------------------------------------------
    // Start
	//------------------------------------------------------------------------------------------
    private void Start()
    {
        // バージョン
        var version = Application.version;
        // リリース
        var companyName = Application.companyName;

        // テキストに挿入
        copyright.text = $"Ver.{version} © 2020 {companyName}";
    }
}
