using System.Collections;
using System.Collections.Generic;
using UnityEngine;
using System;
using Common;
//==============================================================================================
public class ButtonController : MonoBehaviour
{
    //------------------------------------------------------------------------------------------
    // member variable
    //------------------------------------------------------------------------------------------
    RectTransform rectTrasform;
    [SerializeField]
    Vector3 startPos = new Vector3(-440, 300, 0);
    float angle;
    //------------------------------------------------------------------------------------------
    // Awake
    //------------------------------------------------------------------------------------------
    private void Awake()
    {
        rectTrasform = GetComponent<RectTransform>();
    }

	//------------------------------------------------------------------------------------------
    // Start
	//------------------------------------------------------------------------------------------
    private void Start()
    {
        
    }

	//------------------------------------------------------------------------------------------
    // Update
	//------------------------------------------------------------------------------------------
	private void Update()
    {
        var pos = this.transform.position;
        rectTrasform.anchoredPosition = new Vector3(startPos.x + (Mathf.Sin(angle - 90.0f) * 30.0f), startPos.y, startPos.z);

        angle += 0.1f;
    }
}
